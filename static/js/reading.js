// ── Scroll Reveal ────────────────────────────
(function initReveal() {
  const sections = document.querySelectorAll('.reveal-section');
  if (!sections.length) return;

  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry, i) => {
      if (entry.isIntersecting) {
        setTimeout(() => {
          entry.target.classList.add('visible');
        }, i * 80);
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.08 });

  sections.forEach(s => observer.observe(s));
})();


// ── Reading Data from template ────────────────
function getReadingData() {
  const el = document.getElementById('readingData');
  if (!el) return {};
  try { return JSON.parse(el.textContent); } catch { return {}; }
}


// ── Build plain-text reading for copy/export ──
function buildPlainText(d) {
  const planets = d.planets || {};
  const lines = [
    '═'.repeat(60),
    '       CELESTIA — YOUR ASTROLOGICAL READING',
    '═'.repeat(60),
    '',
    `  Name          : ${d.name || ''}`,
    `  Date of Birth : ${d.dob || ''}`,
    `  Birth Time    : ${d.birth_time || ''}`,
    `  Birth Place   : ${d.birth_place || ''}`,
    '',
    '─'.repeat(60),
    '  THE BIG THREE',
    '─'.repeat(60),
    `  ☀  Sun Sign    : ${d.sun_sign || ''}`,
    `  🌙 Moon Sign   : ${d.moon_sign || ''}`,
    `  ↑  Rising Sign : ${d.rising_sign || ''}`,
    '',
    '─'.repeat(60),
    '  PERSONALITY & STRENGTHS',
    '─'.repeat(60),
    d.sun_strengths || '',
    '',
    '─'.repeat(60),
    '  GROWTH & CHALLENGES',
    '─'.repeat(60),
    d.sun_challenges || '',
    '',
    '─'.repeat(60),
    '  YOUR EMOTIONAL WORLD',
    '─'.repeat(60),
    d.moon_interpretation || '',
    '',
    '─'.repeat(60),
    '  YOUR OUTER PRESENCE',
    '─'.repeat(60),
    d.rising_interpretation || '',
    '',
    '─'.repeat(60),
    '  LOVE & RELATIONSHIPS',
    '─'.repeat(60),
    d.sun_love || '',
    '',
    '─'.repeat(60),
    '  CAREER & VOCATION',
    '─'.repeat(60),
    d.sun_career || '',
    '',
    '─'.repeat(60),
    '  LIFE PATH',
    '─'.repeat(60),
    d.life_path || '',
    '',
  ];

  if (Object.keys(planets).length) {
    lines.push('─'.repeat(60));
    lines.push('  PLANETARY POSITIONS');
    lines.push('─'.repeat(60));
    for (const [name, p] of Object.entries(planets)) {
      const retro = p.retrograde ? ' ℞' : '';
      lines.push(`  ${(p.emoji || '★').padEnd(3)} ${name.padEnd(14)}: ${p.sign}${retro} (${p.degree}°)`);
    }
    lines.push('');
  }

  lines.push('═'.repeat(60));
  lines.push('  Generated by Celestia');
  lines.push('═'.repeat(60));

  return lines.join('\n');
}


// ── Copy to Clipboard ─────────────────────────
async function copyReading() {
  const d = getReadingData();
  const text = buildPlainText(d);

  try {
    await navigator.clipboard.writeText(text);
    showToast();
  } catch {
    // Fallback
    const ta = document.createElement('textarea');
    ta.value = text;
    ta.style.position = 'fixed';
    ta.style.opacity = '0';
    document.body.appendChild(ta);
    ta.focus();
    ta.select();
    document.execCommand('copy');
    document.body.removeChild(ta);
    showToast();
  }
}

function showToast() {
  const toast = document.getElementById('copyToast');
  if (!toast) return;
  toast.classList.remove('hidden');
  setTimeout(() => toast.classList.add('hidden'), 3000);
}


// ── Export as Text ────────────────────────────
async function exportAsText() {
  const d = getReadingData();
  try {
    const res = await fetch('/export/text', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(d),
    });
    if (!res.ok) throw new Error('Export failed');
    const blob = await res.blob();
    const url  = URL.createObjectURL(blob);
    const a    = document.createElement('a');
    a.href = url;
    a.download = `celestia_reading_${(d.name || 'reading').replace(/\s+/g,'_')}.txt`;
    a.click();
    URL.revokeObjectURL(url);
  } catch (err) {
    alert('Export failed: ' + err.message);
  }
}


// ── Export as PDF ─────────────────────────────
async function exportAsPdf() {
  const d = getReadingData();
  try {
    const res = await fetch('/export/pdf', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(d),
    });
    if (!res.ok) {
      const err = await res.json();
      throw new Error(err.error || 'PDF export failed');
    }
    const blob = await res.blob();
    const url  = URL.createObjectURL(blob);
    const a    = document.createElement('a');
    a.href = url;
    a.download = `celestia_reading_${(d.name || 'reading').replace(/\s+/g,'_')}.pdf`;
    a.click();
    URL.revokeObjectURL(url);
  } catch (err) {
    alert('PDF export failed: ' + err.message);
  }
}


// ── Wire buttons ──────────────────────────────
function wireButtons() {
  const ids = {
    'copyBtn':           copyReading,
    'copyBtnBottom':     copyReading,
    'exportTxtBtn':      exportAsText,
    'exportTxtBtnBottom':exportAsText,
    'exportPdfBtn':      exportAsPdf,
    'exportPdfBtnBottom':exportAsPdf,
  };

  for (const [id, fn] of Object.entries(ids)) {
    const el = document.getElementById(id);
    if (el) el.addEventListener('click', fn);
  }
}

document.addEventListener('DOMContentLoaded', wireButtons);
